const Sidebar = () => {
  const [categories, setCategories] = useState<Category[]>([]);
  const pathname = usePathname();
  const currentCategoryId = pathname.startsWith('/category/') ? pathname.split('/')[2] : null;

  useEffect(() => {
    fetch('/api/categories')
      .then(res => res.json())
      .then(data => {
        setCategories(data);
      })
      .catch(error => {
        console.error('Error fetching categories:', error);
      });
  }, []);

  return (
    <div 
      className="group bg-gray-800 shadow-lg rounded-lg transition-all duration-300 w-12 hover:w-48 h-[calc(100vh-2rem)] overflow-hidden"
      style={{
        clipPath: 'inset(0)',
        clip: 'rect(0, auto, auto, 0)'
      }}
    >
      <div className="h-full w-12 group-hover:w-48 transition-all duration-300 overflow-hidden">
        {/* Title */}
        <div className="p-3 flex items-center text-gray-300">
          <svg
            className="w-5 h-5 transition-transform group-hover:rotate-180 flex-shrink-0"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M9 5l7 7-7 7"
            />
          </svg>
          <span className="font-bold text-sm ml-2 opacity-0 group-hover:opacity-100 transition-opacity duration-300 whitespace-nowrap">
            Categories
          </span>
        </div>

        {/* Category list */}
        <div className="space-y-1 px-3 overflow-y-auto hide-scrollbar h-[calc(100%-3rem)]">
          {/* Home link */}
          <Link
            href="/"
            className={`flex items-center rounded-md transition-all duration-300 py-1.5 text-gray-300 hover:bg-gray-700 ${pathname === '/' ? 'bg-gray-700' : ''}`}
          >
            <span className="flex-shrink-0 text-gray-400">
              <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M3 12l2-2m0 0l7-7 7 7M5 10v10a1 1 0 001 1h3m10-11l2 2m-2-2v10a1 1 0 01-1 1h-3m-6 0a1 1 0 001-1v-4a1 1 0 011-1h2a1 1 0 011 1v4a1 1 0 001 1m-6 0h6" />
              </svg>
            </span>
            <span className="ml-2 text-sm truncate opacity-0 group-hover:opacity-100 transition-opacity duration-300 whitespace-nowrap">
              All Games
            </span>
          </Link>

          {/* Category links */}
          {categories.map((category) => (
            <Link
              key={category.id}
              href={`/category/${category.id}`}
              className={`flex items-center rounded-md transition-all duration-300 py-1.5 text-gray-300 hover:bg-gray-700 ${currentCategoryId === category.id.toString() ? 'bg-gray-700' : ''}`}
            >
              <span className="flex-shrink-0 text-gray-400">
                {iconMap[category.category_pilot] || iconMap['default']}
              </span>
              <span className="ml-2 text-sm truncate opacity-0 group-hover:opacity-100 transition-opacity duration-300 whitespace-nowrap">
                {category.name}
                {category.total_games && category.total_games > 0 && (
                  <span className="ml-1 text-xs text-gray-500">({category.total_games})</span>
                )}
              </span>
            </Link>
          ))}
        </div>
      </div>
    </div>
  );
};

export default Sidebar; 